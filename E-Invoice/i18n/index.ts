// ── Subscriber pub-sub so `useTranslation` hooks re-render on language change ──
let currentLang: Lang = ((): Lang => {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    return v === 'ar' ? 'ar' : 'en';
  } catch { return 'en'; }
})();

const subscribers = new Set<(lang: Lang) => void>();

/** Get the current language synchronously (outside React). */
export const getLang = (): Lang => currentLang;

/** Translate a key. Falls back to the English value, then to the key itself. */
export const t = (key: string, fallback?: string): string => {
  return translations[currentLang][key] ?? translations.en[key] ?? fallback ?? key;
};

/**
 * Flip the active language, persist it, update `<html dir>` and notify all
 * subscribers so they can re-render.
 */
export const setLang = (next: Lang): void => {
  if (next !== 'en' && next !== 'ar') return;
  currentLang = next;
  try { localStorage.setItem(STORAGE_KEY, next); } catch {}
  // RTL handling is a single-line HTML attr flip — cheaper than a CSS bundle swap.
  try {
    document.documentElement.dir = next === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = next;
  } catch {}
  subscribers.forEach(s => s(next));
};

// Apply on module load so the first paint picks up the stored language.
try {
  document.documentElement.dir  = currentLang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = currentLang;
} catch {}

/**
 * React hook — rerenders when the global language changes.
 * Returns a tuple-ish object for ergonomic destructuring:
 *   const { t, lang, setLang } = useTranslation();
 */
export function useTranslation() {
  const [lang, setLocalLang] = React.useState<Lang>(currentLang);
  React.useEffect(() => {
    const sub = (l: Lang) => setLocalLang(l);
    subscribers.add(sub);
    return () => { subscribers.delete(sub); };
  }, []);
  return { lang, t, setLang };
}
